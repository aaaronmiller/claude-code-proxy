#!/usr/bin/env bash
# Compression Stack Hardening Script
# Integrates Headroom + RTK compression with all CLI tools
# Usage: ./harden-compression-stack.sh

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
STATE_DIR="${XDG_CONFIG_HOME:-$HOME/.config}/input-compression"
SHIM_DIR="$STATE_DIR/shims"
BIN_DIR="$HOME/.local/bin"

# Colors
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
NC='\033[0m'

log_info() { echo -e "${BLUE}[INFO]${NC} $1"; }
log_success() { echo -e "${GREEN}[✓]${NC} $1"; }
log_warn() { echo -e "${YELLOW}[WARN]${NC} $1"; }
log_error() { echo -e "${RED}[✗]${NC} $1"; }

# ============================================================================
# PREREQUISITE CHECKS
# ============================================================================

check_prerequisites() {
    log_info "Checking prerequisites..."
    
    local missing=()
    
    # Check headroom
    if ! command -v headroom &>/dev/null; then
        missing+=("headroom (pip install headroom-ai[ml])")
    fi
    
    # Check rtk
    if ! command -v rtk &>/dev/null; then
        missing+=("rtk")
    fi
    
    # Check compressctl
    if [[ ! -x "$SCRIPT_DIR/bin/compressctl" ]] && ! command -v compressctl &>/dev/null; then
        missing+=("compressctl (run quickstart.sh first)")
    fi
    
    # Check GPU
    if ! command -v nvidia-smi &>/dev/null; then
        log_warn "NVIDIA GPU not detected - compression will run on CPU"
    else
        log_success "NVIDIA GPU detected"
        nvidia-smi --query-gpu=name,memory.total --format=csv,noheader
    fi
    
    # Check headroom is running
    if pgrep -f "headroom proxy" &>/dev/null; then
        log_success "Headroom proxy running"
    else
        log_error "Headroom proxy NOT running - run: headroom proxy --port 8787 --mode token_headroom"
    fi
    
    if [[ ${#missing[@]} -gt 0 ]]; then
        log_error "Missing: ${missing[*]}"
        return 1
    fi
    
    log_success "All prerequisites met"
}

# ============================================================================
# CREATE HARDENED SHIMS
# ============================================================================

create_shim() {
    local tool="$1"
    local shim_path="$SHIM_DIR/$tool"
    
    cat > "$shim_path" << SHIM_EOF
#!/usr/bin/env bash
# Hardened shim for $tool with Headroom + RTK compression
# Auto-generated by harden-compression-stack.sh

set -euo pipefail

STATE_DIR="\${XDG_CONFIG_HOME:-\$HOME/.config}/input-compression"
STATE_FILE="\$STATE_DIR/state.env"

# Load compression state
if [[ -f "\$STATE_FILE" ]]; then
    source "\$STATE_FILE"
fi

COMPRESSION_ENABLED="\${COMPRESSION_ENABLED:-0}"
HEADROOM_PORT="\${HEADROOM_PORT:-8787}"
CLAUDE_PROXY_URL="\${CLAUDE_PROXY_URL:-http://127.0.0.1:8082}"

# Error handling
handle_error() {
    echo "" >&2
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━" >&2
    echo "  $tool: Compression Stack Error" >&2
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━" >&2
    echo "  Exit code: \$1" >&2
    echo "  Timestamp: \$(date '+%Y-%m-%d %H:%M:%S')" >&2
    
    if [[ "\$COMPRESSION_ENABLED" == "1" ]]; then
        if ! pgrep -f "headroom proxy" &>/dev/null; then
            echo "  → Headroom proxy not running" >&2
            echo "  → Fix: headroom proxy --port \$HEADROOM_PORT --mode token_headroom" >&2
        fi
    fi
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━" >&2
    exit \$1
}

trap 'handle_error \$?' EXIT

# Run with compression if enabled
if [[ "\$COMPRESSION_ENABLED" == "1" ]]; then
    # Set environment for compression
    export OPENAI_BASE_URL="http://127.0.0.1:\$HEADROOM_PORT/v1"
    export PROVIDER_BASE_URL="http://127.0.0.1:\$HEADROOM_PORT/v1"
fi

# Execute the actual tool
exec "$tool" "\$@"
SHIM_EOF
    
    chmod +x "$shim_path"
    log_success "Created shim: $shim_path"
}

# ============================================================================
# CONFIGURE TOOL-SPECIFIC INTEGRATIONS
# ============================================================================

configure_claude() {
    log_info "Configuring Claude Code..."
    
    local settings="$HOME/.claude/settings.json"
    if [[ -f "$settings" ]]; then
        # Backup
        cp "$settings" "$settings.backup.$(date +%Y%m%d)"
        
        # Update to use proxy
        python3 << PYEOF
import json
with open('$settings', 'r') as f:
    config = json.load(f)

# Set proxy URL
if 'env' not in config:
    config['env'] = {}
config['env']['OPENAI_BASE_URL'] = 'http://127.0.0.1:8787/v1'
config['env']['PROVIDER_BASE_URL'] = 'http://127.0.0.1:8787/v1'

with open('$settings', 'w') as f:
    json.dump(config, f, indent=2)
print("✓ Claude settings updated")
PYEOF
    else
        log_warn "Claude settings not found at $settings"
    fi
}

configure_qwen() {
    log_info "Configuring Qwen Code..."
    
    local settings="$HOME/.qwen/settings.json"
    if [[ -f "$settings" ]]; then
        cp "$settings" "$settings.backup.$(date +%Y%m%d)"
        
        python3 << PYEOF
import json
with open('$settings', 'r') as f:
    config = json.load(f)

# Set proxy environment
if 'env' not in config:
    config['env'] = {}
config['env']['OPENAI_BASE_URL'] = 'http://127.0.0.1:8787/v1'
config['env']['PROVIDER_BASE_URL'] = 'http://127.0.0.1:8787/v1'

with open('$settings', 'w') as f:
    json.dump(config, f, indent=2)
print("✓ Qwen settings updated")
PYEOF
    fi
}

configure_codex() {
    log_info "Configuring Codex CLI..."
    
    local config_dir="$HOME/.codex"
    mkdir -p "$config_dir"
    
    cat > "$config_dir/env" << ENV_EOF
# Codex CLI - Compression Stack Integration
OPENAI_BASE_URL=http://127.0.0.1:8787/v1
PROVIDER_BASE_URL=http://127.0.0.1:8787/v1
ENV_EOF
    
    log_success "Codex env configured"
}

configure_opencode() {
    log_info "Configuring OpenCode..."
    
    local config_dir="$HOME/.opencode"
    mkdir -p "$config_dir"
    
    cat > "$config_dir/.env" << ENV_EOF
# OpenCode - Compression Stack Integration
OPENAI_BASE_URL=http://127.0.0.1:8787/v1
PROVIDER_BASE_URL=http://127.0.0.1:8787/v1
ENV_EOF
    
    log_success "OpenCode env configured"
}

configure_openclaw() {
    log_info "Configuring OpenClaw..."
    
    # OpenClaw uses standard OpenAI env vars
    local config="$HOME/.config/openclaw/config.json"
    mkdir -p "$(dirname "$config")"
    
    if [[ -f "$config" ]]; then
        cp "$config" "$config.backup.$(date +%Y%m%d)"
    fi
    
    cat > "$config" << ENV_EOF
{
  "openai_base_url": "http://127.0.0.1:8787/v1",
  "provider_base_url": "http://127.0.0.1:8787/v1",
  "compression_enabled": true
}
ENV_EOF
    
    log_success "OpenClaw configured"
}

configure_hermes() {
    log_info "Configuring Hermes Bot..."
    
    local config="$HOME/.hermes/config.json"
    mkdir -p "$(dirname "$config")"
    
    if [[ -f "$config" ]]; then
        cp "$config" "$config.backup.$(date +%Y%m%d)"
    fi
    
    cat > "$config" << ENV_EOF
{
  "provider": {
    "base_url": "http://127.0.0.1:8787/v1",
    "compression": {
      "enabled": true,
      "headroom_port": 8787,
      "device": "cuda"
    }
  }
}
ENV_EOF
    
    log_success "Hermes configured"
}

# ============================================================================
# CREATE WRAPPER SCRIPTS
# ============================================================================

create_wrapper() {
    local tool="$1"
    local wrapper_path="$BIN_DIR/$tool-compressed"
    
    cat > "$wrapper_path" << WRAPPER_EOF
#!/usr/bin/env bash
# Compressed wrapper for $tool
# Forces compression regardless of global state

export COMPRESSION_ENABLED=1
export HEADROOM_PORT=8787
export OPENAI_BASE_URL="http://127.0.0.1:8787/v1"
export PROVIDER_BASE_URL="http://127.0.0.1:8787/v1"

exec "$tool" "\$@"
WRAPPER_EOF
    
    chmod +x "$wrapper_path"
    log_success "Created wrapper: $wrapper_path"
}

# ============================================================================
# INSTALL SYSTEMD SERVICE
# ============================================================================

install_systemd_service() {
    log_info "Installing systemd service..."
    
    local service_dir="$HOME/.config/systemd/user"
    mkdir -p "$service_dir"
    
    cat > "$service_dir/headroom-proxy.service" << SVC_EOF
[Unit]
Description=Headroom Compression Proxy
After=network.target

[Service]
Type=simple
ExecStart=$BIN_DIR/headroom proxy --port 8787 --mode token_headroom --openai-api-url https://openrouter.ai/api/v1 --backend openrouter --no-telemetry
Restart=on-failure
RestartSec=5
Environment="PATH=$BIN_DIR:/usr/local/cuda/bin:%H"
Environment="CUDA_VISIBLE_DEVICES=0"

[Install]
WantedBy=default.target
SVC_EOF
    
    # Enable the service
    systemctl --user daemon-reload
    systemctl --user enable headroom-proxy.service 2>/dev/null || log_warn "systemd user services not available"
    
    log_success "Systemd service installed"
}

# ============================================================================
# CREATE MONITORING SCRIPT
# ============================================================================

create_monitor() {
    log_info "Creating monitoring script..."
    
    cat > "$BIN_DIR/compression-watch" << WATCH_EOF
#!/usr/bin/env bash
# Real-time compression monitoring

watch -n2 '
echo "=== GPU Status ==="
nvidia-smi --query-gpu=memory.used,utilization.gpu --format=csv,noheader

echo ""
echo "=== Headroom Process ==="
ps aux | grep "headroom proxy" | grep -v grep | awk "{print \"PID: \"\$2\" | CPU: \"\$3\"% | MEM: \"\$4\"%\"}"

echo ""
echo "=== Compression Stats ==="
if [[ -f ~/.local/share/headroom/headroom-default.out ]]; then
    tail -20 ~/.local/share/headroom/headroom-default.out | grep -E "saved|compress" | tail -5
fi
'
WATCH_EOF
    
    chmod +x "$BIN_DIR/compression-watch"
    log_success "Created: compression-watch"
}

# ============================================================================
# MAIN
# ============================================================================

main() {
    echo "╔════════════════════════════════════════════════════════╗"
    echo "║   COMPRESSION STACK HARDENING SCRIPT                  ║"
    echo "║   Headroom + RTK + All CLI Tools Integration          ║"
    echo "╚════════════════════════════════════════════════════════╝"
    echo ""
    
    check_prerequisites || exit 1
    
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    log_info "Creating shims..."
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    
    mkdir -p "$SHIM_DIR"
    for tool in claude qwen codex opencode openclaw hermes; do
        if command -v "$tool" &>/dev/null; then
            create_shim "$tool"
        fi
    done
    
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    log_info "Configuring tool-specific integrations..."
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    
    configure_claude
    configure_qwen
    configure_codex
    configure_opencode
    configure_openclaw
    configure_hermes
    
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    log_info "Creating compressed wrappers..."
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    
    for tool in claude qwen codex opencode openclaw hermes; do
        if command -v "$tool" &>/dev/null; then
            create_wrapper "$tool"
        fi
    done
    
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    log_info "Installing systemd service..."
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
    
    install_systemd_service
    create_monitor
    
    echo ""
    echo "╔════════════════════════════════════════════════════════╗"
    echo "║   HARDENING COMPLETE                                   ║"
    echo "╚════════════════════════════════════════════════════════╝"
    echo ""
    echo "Quick commands:"
    echo "  compressctl on          # Enable compression"
    echo "  compressctl off         # Disable compression"
    echo "  compressctl status      # Check status"
    echo "  compression-watch       # Real-time monitoring"
    echo "  compress-monitor        # Full status report"
    echo ""
    echo "Compressed wrappers:"
    echo "  claude-compressed       # Claude with forced compression"
    echo "  qwen-compressed         # Qwen with forced compression"
    echo "  codex-compressed        # Codex with forced compression"
    echo "  opencode-compressed     # OpenCode with forced compression"
    echo "  openclaw-compressed     # OpenClaw with forced compression"
    echo "  hermes-compressed       # Hermes with forced compression"
    echo ""
    echo "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
}

main "$@"
